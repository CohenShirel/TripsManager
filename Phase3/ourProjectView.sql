-- =======================================================================
-- 1. הכנה ויצירה של המבט (VIEW) - אגף קבוצות, מדריכים ומשתתפים
-- =======================================================================

-- פקודה זו מוחקת את המבט מהזיכרון של מסד הנתונים במידה והוא כבר קיים,
-- כדי למנוע שגיאות של "המבט כבר קיים" (View already exists) כשמריצים את הקוד מחדש.
DROP VIEW IF EXISTS v_group_guide_participants;

-- יצירת מבט חדש (טבלה וירטואלית) שמציג נתונים משולבים מארבע טבלאות שונות.
CREATE VIEW v_group_guide_participants AS
SELECT 
    g.groupid,          -- מזהה הקבוצה (מטבלת הקבוצות)
    g.groupname,        -- שם הקבוצה (מטבלת הקבוצות)
    gd.guidename,       -- שם המדריך (מטבלת המדריכים)
    gd.specialization,  -- תחום ההתמחות של המדריך (מטבלת המדריכים)
    p.participantid,    -- מזהה ייחודי של המטייל (מטבלת המשתתפים)
    p.firstname,        -- שם פרטי של המטייל (מטבלת המשתתפים)
    p.lastname,         -- שם משפחה של המטייל (מטבלת המשתתפים)
    p.age               -- גיל המטייל (מטבלת המשתתפים)
FROM 
    public."GROUP" g    -- נקודת המוצא: טבלת הקבוצות (במירכאות כפולות כי group היא מילה שמורה)
JOIN 
    public.guide gd ON g.guideid = gd.guideid -- חיבור טבלת המדריכים כדי לדעת מי המדריך של כל קבוצה
JOIN 
    public.participantgroup pg ON g.groupid = pg.groupid -- חיבור טבלת הקשר (רבים לרבים) שמקשרת בין קבוצות למטיילים
JOIN 
    public.participant p ON pg.participantid = p.participantid; -- חיבור טבלת המשתתפים כדי לשלוף את הפרטים האישיים של המטיילים בקבוצה


-- =======================================================================
-- 2. שאילתות ניתוח נתונים על גבי המבט שיצרנו
-- =======================================================================

-- שאילתה א': בדיקת עומסים וחלוקת משתתפים למדריכים
-- מה היא עושה? היא פונה למבט שיצרנו, סופרת את כמות המטיילים הייחודיים, ומקבצת אותם לפי שם המדריך וההתמחות שלו.
-- מטרה עסקית: לעזור למנהלי כוח האדם לראות איזה מדריך אחראי על כמות המטיילים הגדולה ביותר.
SELECT 
    guidename, 
    specialization, 
    COUNT(participantid) AS total_participants_assigned -- ספירת סך כל המשתתפים המשובצים למדריך
FROM 
    v_group_guide_participants
GROUP BY 
    guidename, 
    specialization; -- קיבוץ התוצאות לפי מדריך והתמחות


-- שאילתה ב': ניתוח טווח גילאי המטיילים עבור כל מדריך
-- מה היא עושה? היא פונה למבט שיצרנו, ומציגה עבור כל מדריך את הגיל הצעיר ביותר (MIN) והגיל המבוגר ביותר (MAX) מבין כל המשתתפים בקבוצות שלו.
-- מטרה עסקית: לאפשר למדריכים ולמנהלי המערכת להבין את פיזור הגילאים של המטיילים תחת אחריותם, כדי להתאים להם את סגנון ותוכן ההדרכה.
SELECT 
    guidename, 
    specialization, 
    MIN(age) AS youngest_participant_age, -- שליפת הגיל הנמוך ביותר של מטייל בקבוצות המדריך
    MAX(age) AS oldest_participant_age    -- שליפת הגיל הגבוה ביותר של מטייל בקבוצות המדריך
FROM 
    v_group_guide_participants
GROUP BY 
    guidename, 
    specialization; -- קיבוץ התוצאות לפי מדריך והתמחות